    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int selectedRow = jTable1.getSelectedRow();
            String productID = String.valueOf(jTable1.getValueAt(selectedRow, 0));
            sendField.setText(productID);
            this.dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTextField7KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField7KeyReleased
        String productName = jTextField7.getText();
        String query = "SELECT * FROM `product` INNER JOIN `category` ON `category`.`id`=`product`.`category_id` WHERE `name` LIKE '%" + productName + "%'";
        setProductTable(query);
    }//GEN-LAST:event_jTextField7KeyReleased

