    public void setProductTable(String query) {
        try {
            ResultSet productTable = SQLConnector.search(query);
            DefaultTableModel productTableModel = (DefaultTableModel) jTable1.getModel();
            productTableModel.setRowCount(0);
            while (productTable.next()) {
                Vector productRow = new Vector();
                productRow.add(String.format("%08d", productTable.getInt("id")));
                productRow.add(productTable.getString("name"));
                productRow.add(productTable.getString("category.category"));
                productRow.add(productTable.getString("sale_price"));
                productTableModel.addRow(productRow);
            }
            jTable1.setModel(productTableModel);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

