    /**
     * BOT & KOT data loading method
     *
     * @param int barcode
     * @param int productId
     * @param String productDeparment
     * @param Date startDate
     * @param Date endDate
     */
    private void botKotdataLoad(int barcode, int productId, String productDeparment, Date startDate, Date endDate, JTable jTable) {
        //get a defualt table model
        this.tableModel = (DefaultTableModel) jTable.getModel();
        tableRowClear();
        try {
            //search the result
            ResultSet resultSet = SQLConnector.search("SELECT `product`.`id`,`product`.`name`,`stock`.`qty`,`stock`.`cost_price`,`product`.`sale_price` "
                    + "FROM `product` \n"
                    + "INNER JOIN `product_department` ON `product`.`product_department_id`=`product_department`.`id`\n"
                    + "INNER JOIN `grn_item` ON `product`.`id`=`grn_item`.`product_id`\n"
                    + "INNER JOIN `grn` ON `grn_item`.`grn_id`=`grn`.`id`\n"
                    + "INNER JOIN `stock` ON `product`.`id`=`stock`.`product_id`\n"
                    + "WHERE `product`.`id`='" + productId + "' AND `product`.`barcode`='" + barcode + "'"
                    + "AND DATE(`grn`.`date_time`) BETWEEN " + startDate + " "
                    + "AND '" + endDate + "' AND `product_department`.`department`='" + productDeparment + "'");

            if (resultSet.next()) {
                while (resultSet.next()) {
                    double qty = resultSet.getDouble("qty");
                    double salePrice = resultSet.getDouble("sale_price");
                    double costPrice = resultSet.getDouble("cost_price");
                    String productName = resultSet.getString("name");

                    // Calculate revenue and cost for the current product
                    double revenue = qty * salePrice;
                    double value = qty * costPrice;
                    // Calculate profit for the current product
                    double profit = revenue - value;

                    //add the row data 
                    Vector tableRowData = new Vector<>();
                    tableRowData.add(productId);
                    tableRowData.add(productName);
                    tableRowData.add(qty);
                    tableRowData.add(salePrice);
                    tableRowData.add(revenue);
                    tableRowData.add(profit);
                    //add row for the table model
                    tableModel.addRow(tableRowData);
                }
            } else {
                noRowDataMessage();
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

