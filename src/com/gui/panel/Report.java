    /**
     * main process button sales summery panel
     *
     * @param evt
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //get a defualt table model
        this.tableModel = (DefaultTableModel) jTable1.getModel();
        tableRowClear();

        //sorting: get the dates
        String startDate = jTextField1.getText();
        String endDate = jTextField2.getText();

        // validation input
        if (startDate.isEmpty()) {
            warningMessage("Please enter the Start Date");
            return;
        } else if (endDate.isEmpty()) {
            warningMessage("Please enter the End Date");
            return;
        }

        try {
            //search the result
            ResultSet resultSet = SQLConnector.search("SELECT\n"
                    + "    item.product_id,\n"
                    + "    SUM(item.qty) as total_qty,\n"
                    + "    p.sale_price,\n"
                    + "    s.cost_price,\n"
                    + "    p.name\n"
                    + "    FROM (\n"
                    + "    SELECT `product_id`, `qty` FROM `table_invoice_item`\n"
                    + "    WHERE table_invoice_id IN (\n"
                    + "        SELECT id FROM `table_invoice`\n"
                    + "        WHERE `date_time` BETWEEN '" + startDate + "' AND '" + endDate + "'\n"
                    + "    )\n"
                    + "    UNION ALL\n"
                    + "    SELECT `product_id`, `qty` FROM `room_invoice_item`\n"
                    + "    WHERE room_invoice_id IN (\n"
                    + "        SELECT `id` FROM `room_invoice`\n"
                    + "        WHERE `date_time` BETWEEN '" + startDate + "' AND '" + endDate + "'\n"
                    + "    )\n"
                    + "    UNION ALL\n"
                    + "    SELECT `product_id`, `qty` FROM `takeaway_item`\n"
                    + "    WHERE `takeaway_invoice_id` IN (\n"
                    + "        SELECT `id` FROM `takeaway_invoice`\n"
                    + "        WHERE `date_time` BETWEEN '" + startDate + "' AND '" + endDate + "'\n"
                    + "    )\n"
                    + ") as item\n"
                    + "JOIN `product` p ON `item.product_id` = `p.id`\n"
                    + "JOIN `stock` s ON `item.product_id` = `s.product_id`\n"
                    + "GROUP BY `item.product_id`, `p.sale_price`, `s.cost_price`");

            if (resultSet.next()) {
                while (resultSet.next()) {
                    double totalQty = resultSet.getDouble("total_qty");
                    double salePrice = resultSet.getDouble("sale_price");
                    double costPrice = resultSet.getDouble("cost_price");
                    String productName = resultSet.getString("name");
                    int productId = resultSet.getInt("product_id");

                    // Calculate revenue and cost for the current product
                    double revenue = totalQty * salePrice;
                    double cost = totalQty * costPrice;
                    // Calculate profit for the current product
                    double profit = revenue - cost;

                    //add the row data 
                    Vector tableRowData = new Vector<>();
                    tableRowData.add(productId);
                    tableRowData.add(productName);
                    tableRowData.add(totalQty);
                    tableRowData.add(salePrice);
                    tableRowData.add(revenue);
                    tableRowData.add(profit);
                    //add row for the table model
                    tableModel.addRow(tableRowData);
                }
            } else {
                noRowDataMessage();
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

